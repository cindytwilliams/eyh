	                        <h1>Expanding Your Horizons Conference</h1>
                          
                          <p>Join us on Saturday, October 6th, 2018 for the 3rd Annual Expanding Your Horizons (EYH) Conference at <a href="https://www.volstate.edu" target="_blank">Volunteer State Community College</a>. Girls in 5th through 9th grades will gather at the Gallatin campus from 8am to 3pm.</p>
                          
                          <p>The conference with begin with a panel of successful women in STEM sharing their life and career experiences, followed by a question and answer session for girls and parents. Each girl will then join a group and attend three hands-on workshops involving chemistry, biology, math, health science, animal science, computer programming and STEM.</p>
                          
                          <p>The cost is $20 per girl and includes workshop materials, lunch, and a t-shirt. Scholarships are available.</p>
                          
                          <p><a class="btn btn-primary custom" href="_files/documents/EYHflyer.pdf" target="_blank" title="Expanding Your Horizons Flyer">View the EYH Flyer</a></p>
                        
                          <h2>Helping Girls Develop a Passion for STEM</h2>

                          <p><img class="img-responsive eyh-logo" src="_files/images/eyh-logo.png" width="215" alt="Expanding Your Horizons Network logo" align="left" />The mission of <a href="https://www.eyhn.org/" target="_blank">Expanding Your Horizons Network</a> is to inspire girls to recognize their potential and pursue opportunities in science, technology, engineering and mathematics.</p>

                          <p>Expanding Your Horizons Network is known as the preeminent source for resources and experiences that provide focused engagement of middle school girls from all backgrounds in STEM (science, technology, engineering and math). At its core, the EYHN’s unique nationwide network of STEM conferences provides a gateway for empowering girls to see themselves as future participants in STEM and STEM-related careers.</p>

                          <p>There are more than 80 EYH conferences each year nationwide with up to 25,000 girls attending. Our Vol State EYH conference started in 2016 and has been growing each year. </p>
                          
