                            <h1>Past Workshops</h1>
                            
                            <img class="img-responsive" src="_files/images/workshop2.png" alt="girls participating in workshops" align="right">
                            
                            <h2 class="workshop">Insect GENEration</h2>
                            <p>Presenters: Meagan Postema and Sarah Maddox<br>
                            Concentration: Genetics</p>
                            <p>In this workshop students will build a model insect based upon genetic information we will provide to them. Gene forms (alleles) contributed by each parent are determined by tossing a coin with one side representing the dominant form of the gene and the other side representing the recessive form. Students will record the genotype and phenotype for each trait and then construct the insect using provided materials.</p>

                            <h2 class="workshop">Your Analysis: Urinalysis and Toxicological Testing in a Clinical Chemistry Laboratory</h2>
                            <p>Presenter: Dr. Jennifer McKenzie<br>
                            Concentration: Clinical Chemistry Laboratory<br>
                            <p>Individuals of all levels of education and training come together in a clinical laboratory to provide critical information about a patient's health to medical professionals. This workshop will provide an introduction to the many career options (from HS level to PhD or MD) in this field. Participants will learn to interpret the results of a point-of-care test and use provided patient history to order additional testing.</p>
                            
                            <img class="img-responsive" src="_files/images/workshop1.png" alt="girls participating in workshops" align="right">
                                                    
                            <h2 class="workshop">Airway Management</h2>
                            <p>Presenter: Brandie Park<br>
                            Concentration: EMT/Paramedic</p>
                            <p>This workshop would give the girls an opportunity to work with manikin heads and learn how EMT's manage a patient's airway in a fun, interactive environment.</p>
                            
                            <h2 class="workshop">Electric Circuit</h2>
                            <p>Presenter: Kurt Hallquist<br>
                            Concentration: Engineering</p>
                            <p>In this workshop, we will present circuit protection with an experiment to overload a simple low voltage circuit and blow a fuse. </p>

                            <h2 class="workshop">The Bandit's Bad Hair Day</h2>
                            <p>Presenter: Tammy L. Jones<br>
                            Concentration: STEM</p>
                            <p>This workshop introduces students to the STEM concepts behind DNA fingerprinting in a relevant, meaningful context. By taking them through the processes for testing trace evidence, the simulation provides opportunities to learn about the structure of DNA, the use of restriction enzymes to break DNA into specific strands, how they are sequenced and what they mean.</p>

                            <img class="img-responsive" src="_files/images/workshop3.png" alt="girls participating in workshops" align="right">
                            
                            <h2 class="workshop">Digital Trebuchet</h2>
                            <p>Presenter: Kurt Hallquist<br>
                            Concentration: Engineering</p>
                            <p>The project is a Digital Trebuchet where the mechanism is assembled by the students then they try to launch a Ping Pong ball the greatest distance using the least amount of current. </p>

                            <h2 class="workshop">Coding Camp</h2>
                            <p>Presenter: Dr. Chenchutta Jackson<br>
                            Concentration: Computer Science</p>
                            <p>In this workshop, students will learn some basics of computer coding and have fun!</p>


                            <h2 class="workshop">The Color of Vision</h2>
                            <p>Presenter: Dr. Alisha Cornish<br>
                            Concentration: Health Science</p>
                            <p>Why are your eyes blue? Why do you see color? Why are some people colorblind? This workshop explores color as it relates to vision and the eye. Through the use of ophthalmic microscopes and photography equipment, attendees will learn the answers to these questions.</p>

                            <img class="img-responsive" src="_files/images/workshop6.png" alt="girls participating in workshops" align="right">
                            
                            <h2 class="workshop">Dog Heart in Motion</h2>
                            <p>Presenter: Dr. Hope Wright<br>
                            Concentration: Animal Science</p>
                            <p>In this workshop we will look at all aspects of a dog's heart. We will look at a dissected heart, listen to the heart beat, view a radiograph of a dog's heart and view the heart in motion on an ultrasound machine.</p>

                            <h2 class="workshop">Making a Good Impression</h2>
                            <p>Presenters: Marilyn Sweat and Desiree Sutphen<br>
                            Concentration: Health Science</p>
                            <p>In this workshop we will talk about the field of Dental Assisting. We discuss the importance of maintaining good oral hygiene, the proper techniques of brushing and flossing your teeth. We will look at models of teeth that include normal and abnormal teeth and make impressions of the teeth.</p>

                            <h2 class="workshop">That's So Random! An Introductory Exploration of Probability and Random Events</h2>
                            <p>Presenter: Jennifer Seat<br>
                            Concentration: Mathematics</p>
                            <p>In this activity based event participants will be exposed to the foundational ideas of probability and random events. As a group we will explore several ideas related to how events occur and fundamental counting structures. By the end of the workshop, participants should be able to:</p>
                            <ol>
                                <li>Determine if an event is random</li>
                                <li>Identify the composition of a probability trial</li>
                                <li>Discuss how probability influences events around us</li>
                            </ol>

                            <img class="img-responsive" src="_files/images/workshop5.png" alt="girls participating in workshops" align="right">
                            
                            <h2 class="workshop">How's the Water?</h2>
                            <p>Presenter: Erin Bloom<br>
                            Concentration: Biologic Science</p>
                            <p>Summary: Students will have the opportunity to learn how aquatic organisms are used at bioindicators of stream health. Students will learn a variety of sampling methods and will have a chance to make their own identifications of common aquatic organisms from a local stream.</p>
                            <p>* Students attending this workshop will need water shoes/rain boots and a change of clothes in the event that the student gets wet.</p>
                            
                            
                            <h2 class="workshop">Body of Evidence</h2>
                            <p>Presenter: Tammy L. Jones<br>
                            Concentration: STEM</p>
                            <p>Body of Evidence casts students in the role of the forensic anthropologist. It provides them with information from a simulated death investigation and challenges them to identify the victim from a list of missing persons. By matching interactive graphical plots of the data with the victim's state of decomposition, the students unravel the case using the same STEM tools real-life investigators use.</p>

                            <h2 class="workshop">Color My World</h2>
                            <p>Presenter: Cheryl Holdaway<br>
                            Concentration: Chemistry</p>
                            <p>The world is so many different colors. Where do all of these come from? Learn a little about how scientists identify colors and then make things that color. Do some hands on experiments separating colors and coming up with new colors.</p>

                            <img class="img-responsive" src="_files/images/workshop7.png" alt="girls participating in workshops" align="right">

                            <h2 class="workshop">It's Chemically Delicious</h2>
                            <p>Presenter: June Hunter<br>
                            Concentration: Chemistry</p>
                            <p>In this workshop, students will discover the secret of making delicious ice cream without using an ice cream maker. You will learn the theory behind the making of ice cream and also have an opportunity to create your own recipes and make delicious ice cream in 10 min or less.</p>

                            <h2 class="workshop">It's a Gas</h2>
                            <p>Presenter: Dr. Jennifer McKenzie<br>
                            Concentration: Chemistry</p>
                            <p>Many types of chemical reactions produce gas as a by-product. Join us in this hand-on laboratory workshop and use chemistry to create foam, inflate balloons (and more!) and learn about the different types of chemical reactions that make it happen.</p>
                            
                            <h2 class="workshop">Ultrasound--My Super Power is Sound</h2>
                            <p>Presenter: Jennifer Carroll<br>
                            Concentration: Health Science</p>
                            <p>In this workshop, we will focus on observing and performing ultrasounds on the human body! During the workshop we will discuss the role of the sonographer, exams performed as well as a hands on opportunity for participants to scan.</p>

                            <img class="img-responsive" src="_files/images/workshop4.png" alt="girls participating in workshops" align="right">
                            
                            <h2 class="workshop">Get Results with Medical Laboratory Science!</h2>
                            <p>Presenter: Kimberly Helton<br>
                            Concentration: Health Science</p>
                            <p>In this workshop we will talk about the exciting field of medical laboratory science. Everyday clinical laboratorians use biology, chemistry, and technology to help physicians diagnose a wide range of illnesses and conditions. It's a career where you can use your knowledge of science to truly improve the lives of others! During the workshop, we will learn specifically about ABO and Rh blood types, their importance in blood transfusion, and how they can sometimes be used to help solve crimes. Finally, you'll have an opportunity to solve your own mystery using laboratory testing!</p>

                            <h2 class="workshop">Adult Workshop: Pathways to Cybersecurity Careers</h2>
                            <p>Presenter: Patricia Anderson<br>
                            Concentration: Cybersecurity</p>
                            <p>Summary: In this workshop we will talk about what you need to know about scholarships, internships, and careers in cybersecurity.</p>
