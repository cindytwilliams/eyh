                            <h1><img class="img-responsive mobile-hide" src="_files/images/photos-essay.png" alt="photos of girls participating in the conference" align="right">Essay Contest</h1>

                            <h2>Purpose</h2>

                            <p>To promote interest and passion for Science, Technology, Engineering, and Math (STEM) subjects, majors, and careers among girls.</p>

                            <h2>Eligibility</h2>

                            <p>Vol State EYH Essay Competition is open to all girls in 5th through 9th grades who are interested in competing for the best essay about a STEM job or subject.</p>

                            <h2>Essay Requirements</h2>

                            <ol>
                                <li>Select a non-healthcare STEM job or subject that you are interested in, and write an essay of a minimum of 700 words describing its current development, its history, and its future. Write about why you are interested in this particular area and how you see yourself being involved in that area in the future. Also, address any experience or future plans that you may have in the area through research or job shadowing.  Examples, data, and pictures are strongly encouraged. </li>
                                <li>Submit the essay by September 15th.</li>
                                <li>Include a bibliography (a list of the sources you used in preparation of your essay).</li>
                                <li>Include references (in the bibliography) and citations (in the text).</li>
                                <li>Libraries, universities, and government sources are recommended. Wikipedia is generally not recommended as the quality of the material published there may not be known.</li>
                                <li>Include your contact information and a writing about yourself as the author.</li>
                            </ol>

                            <h2>Deadline</h2>

                            <p>Full essays are to be submitted to <a href="mailto:lingli.ni@volstate.edu?subject=EYH Essay Contest">lingli.ni@volstate.edu</a> by September 15th for the prize competition. The top three competitors will be announced and contacted prior to the conference. The awards ceremony will be held at the Vol State EYH conference on the first Saturday in October.</p>

